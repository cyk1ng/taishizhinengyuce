    INDEX `idx_plan_type` (`PLAN_TYPE`),
    INDEX `idx_start_time` (`START_TIME`),
    INDEX `idx_status` (`STATUS`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='周计划表';

