    `FAULT_ID` VARCHAR(36) NOT NULL COMMENT '故障ID',
    `FAULT_NO` VARCHAR(64) DEFAULT NULL COMMENT '故障编号',
    `FAULT_TYPE` VARCHAR(20) DEFAULT 'unknown' COMMENT '故障类型(known确定/unknown不确定)',
    `FAULT_LEVEL` VARCHAR(20) DEFAULT NULL COMMENT '故障等级',
    `EQUIPMENT_NAME` VARCHAR(255) DEFAULT NULL COMMENT '故障设备名称',
    `EQUIPMENT_ID` VARCHAR(64) DEFAULT NULL COMMENT '故障设备ID',
    `FAULT_LOCATION` VARCHAR(255) DEFAULT NULL COMMENT '故障位置',
    `FAULT_TIME` DATETIME DEFAULT NULL COMMENT '跳闸时间',
    `RESTORE_TIME` DATETIME DEFAULT NULL COMMENT '预计恢复送电时间',
    `ACTUAL_RESTORE_TIME` DATETIME DEFAULT NULL COMMENT '实际恢复时间',
    `RECLOSE_RESULT` VARCHAR(20) DEFAULT 'fail' COMMENT '重合结果(success成功/fail失败)',
    `FAULT_REASON` TEXT COMMENT '故障原因',
    `HANDLE_MEASURE` TEXT COMMENT '处理措施',
    `STATUS` VARCHAR(20) DEFAULT 'handling' COMMENT '状态(handling处理中/resolved已终结)',
    `HANDLER_ID` VARCHAR(64) DEFAULT NULL COMMENT '处理人ID',
    `HANDLER_NAME` VARCHAR(64) DEFAULT NULL COMMENT '处理人姓名',
    `CITY_DEPT_ID` VARCHAR(36) DEFAULT NULL COMMENT '所属地市ID',
    `CITY_DEPT_NAME` VARCHAR(255) DEFAULT NULL COMMENT '所属地市名称',
    `CREATE_TIME` DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    `UPDATE_TIME` DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    PRIMARY KEY (`FAULT_ID`),
    INDEX `idx_fault_no` (`FAULT_NO`),
    INDEX `idx_fault_time` (`FAULT_TIME`),
    INDEX `idx_reclose_result` (`RECLOSE_RESULT`),
    INDEX `idx_status` (`STATUS`),
    INDEX `idx_equipment` (`EQUIPMENT_ID`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='故障日志表';

