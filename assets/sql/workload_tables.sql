-- 1.1 检修业务表 (maintenance_records)
-- 用于记录停电检修、复电等业务
CREATE TABLE IF NOT EXISTS `maintenance_records` (
    `RECORD_ID` VARCHAR(36) NOT NULL COMMENT '记录ID',
    `WORK_ORDER_NO` VARCHAR(64) DEFAULT NULL COMMENT '工单编号',
    `OPERATION_TYPE` VARCHAR(20) DEFAULT NULL COMMENT '操作类型(power_off停电/power_on复电)',
    `ORDER_TYPE` VARCHAR(20) DEFAULT 'network' COMMENT '下令方式(phone电话令/network网络令)',
    `EQUIPMENT_NAME` VARCHAR(255) DEFAULT NULL COMMENT '设备名称',
    `EQUIPMENT_ID` VARCHAR(64) DEFAULT NULL COMMENT '设备ID',
    `VOLTAGE_LEVEL` VARCHAR(20) DEFAULT NULL COMMENT '电压等级',
    `MAINTENANCE_CONTENT` TEXT COMMENT '检修内容',
    `PLAN_START_TIME` DATETIME DEFAULT NULL COMMENT '计划开始时间',
    `PLAN_END_TIME` DATETIME DEFAULT NULL COMMENT '计划结束时间',
    `ACTUAL_START_TIME` DATETIME DEFAULT NULL COMMENT '实际开始时间(批复停电时间)',
    `ACTUAL_END_TIME` DATETIME DEFAULT NULL COMMENT '实际结束时间(复电时间)',
    `START_TIME` DATETIME DEFAULT NULL COMMENT '操作开始时间(用于工作量统计)',
    `END_TIME` DATETIME DEFAULT NULL COMMENT '操作结束时间',
    `STATUS` VARCHAR(20) DEFAULT 'pending' COMMENT '状态(pending待执行/executing执行中/completed已终结)',
    `OPERATOR_ID` VARCHAR(64) DEFAULT NULL COMMENT '操作人ID',
    `OPERATOR_NAME` VARCHAR(64) DEFAULT NULL COMMENT '操作人姓名',
    `CITY_DEPT_ID` VARCHAR(36) DEFAULT NULL COMMENT '所属地市ID',
    `CITY_DEPT_NAME` VARCHAR(255) DEFAULT NULL COMMENT '所属地市名称',
    `CREATE_TIME` DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    `UPDATE_TIME` DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    PRIMARY KEY (`RECORD_ID`),
    INDEX `idx_work_order` (`WORK_ORDER_NO`),
    INDEX `idx_start_time` (`START_TIME`),
    INDEX `idx_operation_type` (`OPERATION_TYPE`),
    INDEX `idx_status` (`STATUS`),
    INDEX `idx_date` (`START_TIME`, `END_TIME`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='检修业务表';

-- 1.2 转供电/方式单表 (transfer_orders)
-- 用于记录转供电操作
CREATE TABLE IF NOT EXISTS `transfer_orders` (
    `ORDER_ID` VARCHAR(36) NOT NULL COMMENT '方式单ID',
    `ORDER_NO` VARCHAR(64) DEFAULT NULL COMMENT '方式单编号',
    `TRANSFER_TYPE` VARCHAR(20) DEFAULT 'transfer' COMMENT '类型(transfer转出/restore恢复)',
    `EQUIPMENT_NAME` VARCHAR(255) DEFAULT NULL COMMENT '设备名称',
    `EQUIPMENT_ID` VARCHAR(64) DEFAULT NULL COMMENT '设备ID',
    `TRANSFER_REASON` TEXT COMMENT '转供电原因',
    `TRANSFER_OUT_TIME` DATETIME DEFAULT NULL COMMENT '批复转出时间',
    `TRANSFER_BACK_TIME` DATETIME DEFAULT NULL COMMENT '批复恢复时间',
    `START_TIME` DATETIME DEFAULT NULL COMMENT '操作开始时间(用于工作量统计)',
    `END_TIME` DATETIME DEFAULT NULL COMMENT '操作结束时间',
    `STATUS` VARCHAR(20) DEFAULT 'pending' COMMENT '状态',
    `OPERATOR_ID` VARCHAR(64) DEFAULT NULL COMMENT '操作人ID',
    `OPERATOR_NAME` VARCHAR(64) DEFAULT NULL COMMENT '操作人姓名',
    `CITY_DEPT_ID` VARCHAR(36) DEFAULT NULL COMMENT '所属地市ID',
    `CITY_DEPT_NAME` VARCHAR(255) DEFAULT NULL COMMENT '所属地市名称',
    `CREATE_TIME` DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    `UPDATE_TIME` DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    PRIMARY KEY (`ORDER_ID`),
    INDEX `idx_order_no` (`ORDER_NO`),
    INDEX `idx_transfer_time` (`TRANSFER_OUT_TIME`, `TRANSFER_BACK_TIME`),
    INDEX `idx_status` (`STATUS`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='转供电/方式单表';

-- 1.3 周计划表 (weekly_plans)
-- 用于记录周计划任务
