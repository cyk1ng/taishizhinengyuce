-- ========================================
-- 4. 工作量统计结果表
-- ========================================

-- 4.1 小时工作量统计表 (hourly_workload_stats)
-- 存储每小时工作量统计结果
CREATE TABLE IF NOT EXISTS `hourly_workload_stats` (
    `STAT_ID` VARCHAR(36) NOT NULL COMMENT '统计ID',
    `STAT_DATE` DATE NOT NULL COMMENT '统计日期',
    `STAT_HOUR` TINYINT NOT NULL COMMENT '统计小时(0-23)',
    `PLAN_TASK_COUNT` INT DEFAULT 0 COMMENT '计划任务数',
    `NON_PLAN_TASK_COUNT` INT DEFAULT 0 COMMENT '非计划任务数',
    `TOTAL_TASK_COUNT` INT DEFAULT 0 COMMENT '总任务数',
    `PLAN_EQUIVALENT` DECIMAL(10,2) DEFAULT 0 COMMENT '计划工作当量',
    `NON_PLAN_EQUIVALENT` DECIMAL(10,2) DEFAULT 0 COMMENT '非计划工作当量',
    `TOTAL_EQUIVALENT` DECIMAL(10,2) DEFAULT 0 COMMENT '总工作当量',
    `STAFF_COUNT` INT DEFAULT 0 COMMENT '当值人数',
    `STAFF_CAPACITY` DECIMAL(10,2) DEFAULT 0 COMMENT '人员工作当量',
    `IS_OVERLOAD` TINYINT DEFAULT 0 COMMENT '是否超负荷(0否/1是)',
    `NEED_EXTRA_STAFF` INT DEFAULT 0 COMMENT '需增派人数',
    `CALC_TIME` DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '计算时间',
    PRIMARY KEY (`STAT_ID`),
    UNIQUE INDEX `idx_date_hour` (`STAT_DATE`, `STAT_HOUR`),
    INDEX `idx_stat_date` (`STAT_DATE`),
    INDEX `idx_is_overload` (`IS_OVERLOAD`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='小时工作量统计表';

-- 4.2 日工作量汇总表 (daily_workload_summary)
