-- 创建非计划工作量统计所需的表
-- 数据库：dispatch_system

-- 1. 创建故障日志表
CREATE TABLE IF NOT EXISTS fault_logs (
    RECORD_ID INT PRIMARY KEY AUTO_INCREMENT COMMENT '记录ID',
    FAULT_ID VARCHAR(50) NOT NULL COMMENT '故障编号',
    FAULT_TYPE VARCHAR(50) NOT NULL COMMENT '故障类型：known-确定故障, unknown-不确定故障, bus_ground-母线接地',
    RECLOSER_RESULT VARCHAR(20) NOT NULL COMMENT '重合闸结果：success-成功, fail-失败',
    EQUIPMENT_NAME VARCHAR(100) NOT NULL COMMENT '设备名称',
    VOLTAGE_LEVEL VARCHAR(20) COMMENT '电压等级',
    FAULT_TIME DATETIME NOT NULL COMMENT '故障发生时间',
    EXPECTED_RESTORE_TIME DATETIME COMMENT '预计恢复时间',
    ACTUAL_RESTORE_TIME DATETIME COMMENT '实际恢复时间',
    IS_HANDED_OVER TINYINT DEFAULT 0 COMMENT '是否已交班：0-未交班, 1-已交班',
    STATUS VARCHAR(20) NOT NULL COMMENT '状态：pending-待处理, processing-处理中, resolved-已解决',
    DUTY_OFFICER VARCHAR(50) COMMENT '当值人员',
    CREATED_TIME DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    UPDATED_TIME DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    INDEX idx_fault_time (FAULT_TIME),
    INDEX idx_is_handed_over (IS_HANDED_OVER),
    INDEX idx_status (STATUS)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='故障日志表';

-- 2. 创建缺陷记录表
CREATE TABLE IF NOT EXISTS defect_records (
    RECORD_ID INT PRIMARY KEY AUTO_INCREMENT COMMENT '记录ID',
    DEFECT_ID VARCHAR(50) NOT NULL COMMENT '缺陷编号',
    DEFECT_TYPE VARCHAR(50) NOT NULL COMMENT '缺陷类型',
    DEFECT_LEVEL VARCHAR(20) NOT NULL COMMENT '缺陷等级：critical-紧急, major-重大, general-一般',
    EQUIPMENT_NAME VARCHAR(100) NOT NULL COMMENT '设备名称',
    DEFECT_TIME DATETIME NOT NULL COMMENT '缺陷发现时间',
    EXPECTED_FIX_TIME DATETIME COMMENT '预计修复时间',
    ACTUAL_FIX_TIME DATETIME COMMENT '实际修复时间',
    IS_HANDED_OVER TINYINT DEFAULT 0 COMMENT '是否已交班：0-未交班, 1-已交班',
    STATUS VARCHAR(20) NOT NULL COMMENT '状态：pending-待处理, processing-处理中, fixed-已修复',
    REPORTER VARCHAR(50) COMMENT '上报人',
    CREATED_TIME DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    UPDATED_TIME DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    INDEX idx_defect_time (DEFECT_TIME),
    INDEX idx_is_handed_over (IS_HANDED_OVER),
    INDEX idx_status (STATUS)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='缺陷记录表';

-- 3. 创建重过载记录表
CREATE TABLE IF NOT EXISTS overload_records (
    RECORD_ID INT PRIMARY KEY AUTO_INCREMENT COMMENT '记录ID',
    OVERLOAD_ID VARCHAR(50) NOT NULL COMMENT '过载编号',
    OVERLOAD_TYPE VARCHAR(20) NOT NULL COMMENT '过载类型：overload-过载, heavy_load-重载',
    EQUIPMENT_NAME VARCHAR(100) NOT NULL COMMENT '设备名称',
    LOAD_RATE DECIMAL(5,2) NOT NULL COMMENT '负载率（百分比）',
    RATED_CAPACITY DECIMAL(10,2) COMMENT '额定容量',
    ACTUAL_LOAD DECIMAL(10,2) COMMENT '实际负载',
    RECORD_TIME DATETIME NOT NULL COMMENT '记录时间',
    EXPECTED_RESOLVE_TIME DATETIME COMMENT '预计解决时间',
    ACTUAL_RESOLVE_TIME DATETIME COMMENT '实际解决时间',
    IS_RESOLVED TINYINT DEFAULT 0 COMMENT '是否已解决：0-未解决, 1-已解决',
    STATUS VARCHAR(20) NOT NULL COMMENT '状态：pending-待处理, monitoring-监控中, resolved-已解决',
    MONITOR_PERSON VARCHAR(50) COMMENT '监控人员',
    CREATED_TIME DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    UPDATED_TIME DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    INDEX idx_record_time (RECORD_TIME),
    INDEX idx_is_resolved (IS_RESOLVED),
    INDEX idx_status (STATUS)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='重过载记录表';

